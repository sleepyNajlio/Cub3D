#include "cub3d.h"

int is_map_el(char *line)
{
    int i;

    i = 0;
    while (line[i] = ' ' || line[i] == '\t')
        i++;
    if (line[i] == '1' || line[i] == '0')
        return (1);
    else
        return (0);
}